#include "header.h"
#include <stdio.h>
#include <stdlib.h>

/**
 * add - returns sum @a and @b
 *
 * @a: int
 * @b: int
 * Return: sum of @a and @b
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - returns @a - @b
 *
 * @a: int
 * @b: int
 * Return: difference of @a and @b
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - returns @a * @b
 *
 * @a: int
 * @b: int
 * Return: product of @a and @b
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - returns @a / @b
 *
 * @a: int
 * @b: int
 * Return: quotient of @a and @b
 */

int divide(int a, int b)
{
	return (a / b);
}

/**
 * mod - returns @a % @b
 *
 * @a: int
 * @b: int
 * Return: modulus of @a and @b
 */

int mod(int a, int b)
{
	return (a % b);
}
